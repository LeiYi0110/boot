package com.bjxc.school;

public class CoachAppSetting {
	
	private Integer id;
	private Integer publishAppointment;
	private Integer assignStudent;
	private Integer maxPublishDays;
	private Integer insId;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getPublishAppointment() {
		return publishAppointment;
	}
	public void setPublishAppointment(Integer publishAppointment) {
		this.publishAppointment = publishAppointment;
	}
	public Integer getAssignStudent() {
		return assignStudent;
	}
	public void setAssignStudent(Integer assignStudent) {
		this.assignStudent = assignStudent;
	}
	public Integer getMaxPublishDays() {
		return maxPublishDays;
	}
	public void setMaxPublishDays(Integer maxPublishDays) {
		this.maxPublishDays = maxPublishDays;
	}
	public Integer getInsId() {
		return insId;
	}
	public void setInsId(Integer insId) {
		this.insId = insId;
	}
	
	
	
	

}


